              <li class="list-text">
                Direct and Indirect Composite Restorations
              </li>
              <p class="text">
                Composite restorations are versatile tooth coloured filling
                materials composed of strong plastic and glass particles that
                are used by our dentists or dental therapists to seal deep
                fissures, fill cavities, and sometimes restore extensively
                broken down teeth.
                <br />
                <br />
                In direct tooth restoration, all of the work is done and
                completed within the mouth. The procedure is commonly referred
                to as filling and it involves the placement of a malleable
                substance into a prepared and cleaned cavity. The material is
                then hardened to restore the structure (and sometimes
                appearance) of the damaged tooth.
                <br />
                <br />
                We offer both direct and indirect restorations for your dental
                needs.
              </p>
              <li class="list-text">Glass Ionomer</li>
              <p class="text">
                Glass Ionomers or Glass Ionomer Cement (GIC) are restorative
                materials which can also be used as base for a composite resin
                overlay where the remaining tooth structure is unsupported and
                requires a bonded composite to maintain its structural
                integrity.
                <br />
                <br />
                Glass ionomer is a versatile dental material with several
                purposes and clinical applications. Its unique properties make
                it suitable for a range of dental procedures.
                <br />
                <br />
                Glass ionomer is commonly used for the restoration of decayed
                teeth, particularly in non-load-bearing areas. It also serves as
                a luting agent for the cementation of crowns and bridges. Its
                adhesive properties contribute to the retention of these
                restorations.
                <br />
                <br />
                Glass ionomer is utilized for placing pit and fissure sealants
                on the occlusal surfaces of molars and premolars. The material
                helps create a protective barrier against decay in these
                vulnerable areas.
              </p>
              <li class="list-text">Sealants</li>
              <p class="text">
                Dental sealants are thin, protective coatings applied to the
                chewing surfaces of the back teeth (molars and premolars). These
                sealants are typically made of a tooth-colored or clear resin
                material and are used as a preventive measure to help protect
                the teeth from decay.
                <br />
                <br />
                The primary purpose of dental sealants is to create a physical
                barrier on the tooth's surface, particularly on the deep grooves
                and pits of the molars and premolars. This barrier helps to
                prevent the accumulation of food particles and bacteria in these
                areas, reducing the risk of tooth decay and cavities.
                <br />
                <br />
                Dental sealants act as a protective shield, preventing food
                particles and bacteria from settling into the natural grooves
                and fissures on the chewing surfaces of the back teeth. By
                sealing these vulnerable areas, the likelihood of tooth decay is
                significantly reduced.
              </p>
              <li class="list-text">Inlays and Onlays</li>
              <p class="text">
                Dental inlays are custom-made restorations that fit into the
                prepared cavity of a tooth. They are fabricated outside the
                mouth and then cemented or bonded into place. Inlays are
                typically used to restore a tooth with moderate to severe decay
                or damage, providing a durable and natural-looking solution. A
                dental inlay is made of a solid material that is custom-fit to
                your tooth. It's usually used to fill the space in the center of
                your tooth that has been damaged by decay or injury.
                <br />
                <br />
                Dental inlays offer a conservative and effective solution for
                restoring teeth with moderate damage or decay. They provide
                durability, aesthetics, and functional restoration while
                preserving healthy tooth structure.
                <br />
                <br />
                A dental onlay in the other hand, are made of a solid material
                that is custom-fit to your tooth. They can also be shaped and
                colored to match your natural teeth. An onlay is larger than an
                inlay but smaller than a dental crown.
                <br />
                <br />
                A dental onlay is a custom-made restoration that covers the
                chewing surface of a tooth, including one or more cusps (points)
                and the surrounding areas. They are fabricated outside the mouth
                and are then cemented or bonded onto the prepared tooth,
                providing a durable and often aesthetically pleasing solution.
                <br />
                <br />
                Dental onlays are used to restore teeth that have significant
                damage or decay, especially when the affected area extends to
                one or more cusps. Onlays are suitable for cases where the
                damage is too extensive for a filling but does not necessitate a
                full dental crown.
                <br />
                <br />
                Similar to inlays, onlays are designed to preserve as much
                healthy tooth structure as possible. They provide a conservative
                approach to restoring teeth by covering only the damaged or
                decayed areas while maintaining the integrity of the remaining
                tooth.
              </p>
              <li class="list-text">Veneers</li>
              <p class="text">
                Veneers are thin, custom-made shells typically made of porcelain
                or composite resin that are designed to cover the front surface
                of teeth. These shells are bonded to the teeth to improve their
                appearance and address various cosmetic concerns. They are a
                popular cosmetic dentistry option for individuals seeking a
                long-lasting and natural-looking solution to improve the
                appearance of their teeth.
                <br />
                <br />
                Veneers are used for many purposes such as cosmetic improvement,
                alignment and spacing, and reshaping. Veneers are primarily used
                to improve the appearance of teeth by addressing issues such as
                discoloration, stains, chips, cracks, or irregularities in size
                and shape. They can be used to create the illusion of straighter
                teeth and close gaps between teeth, providing a more uniform and
                aesthetically pleasing smile. They can be applied to alter the
                size, length, or shape of teeth, creating a harmonious and
                balanced smile.
              </p>
